											What are they all about?
											</h2>

											
											<i class="fas fa-angle-double-right"></i>&nbsp;&nbsp;
											
											In this non-partisan vegan activist group, we do the following:
<br><br>
Our daily leader scans or monitors a popular non-vegan Facebook page (examples include food and fashion pages, major news outlets). The leader makes a non-aggressive, pro-vegan comment on a post on that page. They post a link to their comment in the DVA group (this link is pinned for the day) As soon as possible after the link is posted, group members Like it (and add replies and additional comments if they choose to do so).
<br><br>
The leader’s comment will rise to the top of its page in proportion to the number of Likes and replies it gets, and the speed at which they arrive! We support one link each day and celebrate each night!  On some days, we will share additional submissions from members for your support.
											
											
											<br><br>		
											<h2 class="post_title mb-20px">
											What else should I know?
											</h2>		

											<i class="fas fa-angle-double-right"></i>&nbsp;&nbsp;
											
											In speaking to others online, we value thoughtful, reasoned, respectful commentary, questions, and the provision of links to help people make the connection and learn about veganism as an ethical position. We absolutely reject being rude, belittling people’s intelligence, their dignity, and any form of discriminatory language (e.g. sexism, racism, ableism, ageism, homophobia, etc.). We are here to raise awareness with a clear vegan message to help create a vegan world.
<br><br>
We also reject any comments that promote less than veganism and abolition as a minimum standard, such as welfarism or reductionism, or any comments that may confuse people regarding veganism as the end goal and minimum standard.  If you want to focus on improving treatment or reducing use, there are many other groups for that. 
											
