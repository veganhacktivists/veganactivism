											<h2 class="post_title mb-20px">
											¿De qué se trata su organización?
											</h2>

											
											Mercy For Animals existe para ponerle fin a la mayor causa de sufrimiento en el planeta: la explotación de animales como alimento, en particular, la industria pecuaria, la acuicultura y la
pesca.
<br><br>
Estas formas de producción de alimentos no solo causan un sufrimiento animal atroz, también tienen efectos perjudiciales para el planeta y las personas.
<br><br>
Mercy For Animals se dedica a erradicar este cruel sistema alimentario y reemplazarlo por uno que no solo sea amable con los animales, sino esencial para el futuro de nuestro planeta y todos los que lo compartimos.
