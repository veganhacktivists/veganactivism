											<h2 class="post_title mb-20px">
											¿Qué tipo de activismo hacen?
											</h2>		

											
Mercy For Animals trabaja en varios programas de defensa de animales confinados en granjas, que a menudo involucran la filmación o difusión de imágenes de sus investigaciones encubiertas en granjas industriales. Difunden estas imágenes principalmente a través de campañas y medios de comunicación en línea. MFA realiza incidencia legal y campañas corporativas a favor de los animales, y llevan a cabo actividades de difusión en persona diseñadas para cambiar la actitud y el comportamiento de los individuos hacia los animales confinados en granjas.
											<br><br>
											
En los últimos años, MFA ha desarrollado sólidos programas de difusión en línea en Estados Unidos e internacionalmente. Sus estadísticas indican que, en los primeros ocho meses de 2016, sus sitios web pro-veganismo se han visto aproximadamente 29.8 millones de veces y su contenido de redes sociales se ha compartido aproximadamente 3.8 millones de veces. Han distribuido Guías vegetarianas para principiantes tanto en formato digital como impreso, y han convencido a casi un millón de personas para que se comprometan a ser vegetarianas. Además de la difusión para individuos, MFA ha llevado a cabo con éxito la difusión corporativa. En los primeros ocho meses de 2016, lograron, por su cuenta o en conjunto con otros grupos, aproximadamente 58 cambios en políticas corporativas. Han ayudado a convencer a las corporaciones a que dejen de producir o usar huevos de gallinas enjauladas y a prohibir la matanza de gallinas explotadas por su carne mientras estén conscientes.
